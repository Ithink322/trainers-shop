$(function () {
  let counter = 0;
  $(".container-blog__questions-section-arrow-img-1").click(function () {
    counter++;
    if (counter % 2 !== 0) {
      $(".container-blog__questions-section-subtext-1").fadeIn(120);
      $(".container-blog__questions-section-arrow-img-1").css(
        "rotate",
        "-90deg"
      );
    } else if (counter % 2 == 0) {
      $(".container-blog__questions-section-subtext-1").fadeOut(0);
      $(".container-blog__questions-section-arrow-img-1").css("rotate", "0deg");
    }
  });
});
